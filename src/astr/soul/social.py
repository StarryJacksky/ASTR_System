def should_join_group(
    *,
    inviter_level: int = 0,
    inviter_familiarity: float = 0.05,
    inviter_affinity: float = 0.0,
    loneliness: float = 0.3,
    irritation: float = 0.0,
    rng: random.Random | None = None,
) -> tuple[bool, float, str]:
    """?????????????/?????????? ????? ????????????????"""
    if inviter_level >= 2:
        return True, 1.0, "????????"
    rng = rng or random
    rel = max(0.5, 0.5 + 1.0 * inviter_familiarity) * (1.0 + 0.5 * inviter_affinity)
    mood = max(0.2, 1.0 + 0.6 * loneliness - 0.8 * irritation)
    p = max(0.0, min(1.0, BASE_JOIN_P * rel * mood))
    return (rng.random() < p), p, "????????"


