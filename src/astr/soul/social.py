def _social_path(soul_name: str):
    return get_settings().soul_package_dir / soul_name / "identity_atlas" / "social.json"


def load_social(soul_name: str = "justin") -> dict:
    p = _social_path(soul_name)
    if p.exists():
        try:
            return json.loads(p.read_text(encoding="utf-8"))
        except Exception:  # noqa: BLE001
            pass
    return {"friends": [], "groups": [], "updated_at": None}


def sync_social(soul_name: str, friends: list[dict], groups: list[dict]) -> dict:
    """???????/???????????"""
    data = {
        "friends": friends,
        "groups": groups,
        "updated_at": datetime.now(UTC).isoformat(),
    }
    p = _social_path(soul_name)
    p.parent.mkdir(parents=True, exist_ok=True)
    p.write_text(json.dumps(data, ensure_ascii=False, indent=2), encoding="utf-8")
    return data


def record_social_note(soul_name: str, note: str) -> None:
    """??????????/???/?????????????????"""
    if not note:
        return
    now = datetime.now(UTC)
    d = (
        get_settings().soul_package_dir
        / soul_name
        / "memory"
        / "chunks"
        / "monologues"
        / now.strftime("%Y-%m")
    )
    d.mkdir(parents=True, exist_ok=True)
    (d / f"social_{int(now.timestamp() * 1000)}.md").write_text(
        f"<!-- ts={now.isoformat()} kind=social -->\n?????{note}\n",
        encoding="utf-8",
    )
